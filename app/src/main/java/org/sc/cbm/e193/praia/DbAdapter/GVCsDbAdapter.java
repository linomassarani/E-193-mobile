    private final Context mCtx;

    private static final String DATABASE_CREATE =
            "CREATE TABLE if not exists " + SQLITE_TABLE + " (" +
                    KEY_ROWID + " integer PRIMARY KEY autoincrement," +
                    KEY_CPF + "," +
                    KEY_NAME + "," +
                    " UNIQUE (" + KEY_CPF +"));";

    public GVC fetchGVCByCPF(String reg) {
        Log.w(TAG, reg);
        Cursor mCursor = null;
        if (reg == null || reg.length() == 0) {
            return null;
        } else {
            mCursor = mDb.query(true, SQLITE_TABLE, new String[]{KEY_ROWID, KEY_CPF,
                            KEY_NAME},
                    KEY_CPF + " like '%" + reg + "%'", null,
                    null, null, KEY_NAME, "50");
        }
        mCursor.moveToNext();

        GVC gvc = new GVC(mCursor.getString(mCursor.getColumnIndex(GVCsDbAdapter.KEY_NAME)),
                mCursor.getString(mCursor.getColumnIndex(GVCsDbAdapter.KEY_CPF)));

        return gvc;
    }

    private static class DatabaseHelper extends SQLiteOpenHelper {

        DatabaseHelper(Context context) {
            super(context, DATABASE_NAME, null, DATABASE_VERSION);
        }

        @Override
        public void onCreate(SQLiteDatabase db) {
            Log.w(TAG, DATABASE_CREATE);
            db.execSQL(DATABASE_CREATE);
            GVCsDbAdapter.insertSomeGVCs(db);
        }

        @Override
        public void onUpgrade(SQLiteDatabase db, int oldVersion, int newVersion) {
            Log.w(TAG, "Upgrading database from version " + oldVersion + " to "
                    + newVersion + ", which will destroy all old data");
            db.execSQL("DROP TABLE IF EXISTS " + SQLITE_TABLE);
            onCreate(db);
        }
    }

    public GVCsDbAdapter(Context ctx) {
        this.mCtx = ctx;
    }

    public GVCsDbAdapter open() throws SQLException {
        mDbHelper = new DatabaseHelper(mCtx);
        mDb = mDbHelper.getWritableDatabase();
//        mDb.execSQL("DROP TABLE IF EXISTS " + SQLITE_TABLE);

        if(!Tools.isTableExists(mDb, SQLITE_TABLE)) {
            mDbHelper.onCreate(mDb);
        }

        return this;
    }

    public void close() {
        if (mDbHelper != null) {
            mDbHelper.close();
        }
    }

    public static long createGVC(String cpf, String name, SQLiteDatabase db) {

        ContentValues initialValues = new ContentValues();
        initialValues.put(KEY_CPF, cpf);
        initialValues.put(KEY_NAME, name);

        return db.insert(SQLITE_TABLE, null, initialValues);
    }

    private boolean deleteAllGVCs() {

        int doneDelete = 0;
        doneDelete = mDb.delete(SQLITE_TABLE, null, null);
        Log.w(TAG, Integer.toString(doneDelete));

        return doneDelete > 0;

    }

    public Cursor fetchGVCsByNameOrRegistration(String inputText) throws SQLException {
        Log.w(TAG, inputText);
        Cursor mCursor = null;
        if (inputText == null || inputText.length() == 0) {
            mCursor = mDb.query(SQLITE_TABLE, new String[]{KEY_ROWID, KEY_CPF
                            , KEY_NAME},
                    null, null, null, null, KEY_NAME, "50");

        } else {
            String key = KEY_NAME;

            if (inputText.matches("^\\d+$")) {
                key = KEY_CPF;
            }

            mCursor = mDb.query(true, SQLITE_TABLE, new String[]{KEY_ROWID, KEY_CPF,
                            KEY_NAME},
                    key + " like '%" + inputText + "%'", null,
                    null, null, KEY_NAME, "50");
        }
        if (mCursor != null) {
            mCursor.moveToFirst();
        }
        return mCursor;

    }

    public Cursor fetchAllGVCs() {

            Cursor mCursor = mDb.query(SQLITE_TABLE, new String[]{KEY_ROWID, KEY_CPF,
                        KEY_NAME},
                null, null, null, null, KEY_NAME, "50");

        if (mCursor != null) {
            mCursor.moveToFirst();
        }
        return mCursor;
    }

